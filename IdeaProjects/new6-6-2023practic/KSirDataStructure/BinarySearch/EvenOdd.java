package KSirDataStructure.BinarySearch;

public class EvenOdd {
    public static void main(String[] args) {
        int n = 5;
        for (int i = 1; i <= n; i=i+2) {
            System.out.print(i+" ");
        }
        System.out.println();
        for (int i = 2; i <= n; i=i+2) {
            System.out.print(i+" ");
        }
    }
}
